const loadSystemStats = async () => {
  try {
    const { data } = await axios.get('/api/analytics/overview?days=30')
    systemStats.value = data
  } catch (err) {
    console.error('Failed to load system stats:', err)
  }
}

const createGame = () => {
  showCreateDialog.value = true
}

const goToDashboard = () => {
  router.push('/dashboard')
}

const goToAnalytics = () => {
  router.push('/analytics')
}

const goToStats = () => {
  router.push('/stats')
}

const goToSimulation = () => {
  router.push('/simulation')
}

const confirmCreate = async () => {
  creating.value = true
  try {
    const res = await apiCreateGame({
      num_players: gameForm.numPlayers,
      small_blind: gameForm.smallBlind,
      big_blind: gameForm.bigBlind
    })

    ElMessage.success('æ¸¸æˆåˆ›å»ºæˆåŠŸ!')
    showCreateDialog.value = false
    router.push(`/game/${res.game_id}`)
  } catch (err) {
    ElMessage.error('åˆ›å»ºå¤±è´¥: ' + (err.message || 'æœªçŸ¥é”™è¯¯'))
  } finally {
    creating.value = false
  }
}

const formatLargeNumber = (num) => {
  if (!num) return '0'
  if (num >= 1000000) return (num / 1000000).toFixed(1) + 'M'
  if (num >= 1000) return (num / 1000).toFixed(1) + 'K'
  return num.toLocaleString()
}

const formatDuration = (seconds) => {
  if (!seconds) return '-'
  const mins = Math.floor(seconds / 60)
  const secs = Math.floor(seconds % 60)
  return `${mins}:${secs.toString().padStart(2, '0')}`
}

onMounted(() => {
  loadSystemStats()
})
